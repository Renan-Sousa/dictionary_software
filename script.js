search = function(){
    query = document.getElementById('search').value;

    if (query == "") {
        return;
    }

    found = -1;

    for (var i = 0; i < dictionary.length; i++) {
        if (query == dictionary[i].word) {
            found = i;
            break;
        }else {
            document.getElementById('word_text').innerHTML = "Palavra não encontrada";
            document.getElementById('definition').innerHTML = "A palavra buscada não pôde ser encontrada\
            no nosso dicionário";
        }
    }

    if (found >= 0) {
        show(found);
    }
}


